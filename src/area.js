export const getLine = () => {
    let arr = JSON.parse(JSON.stringify(center))
    if (arr.length & 1) {
        arr = arr.slice(0, -1)
    }
    const new_arr = arr.sort((a, b) => Math.random() > 0.5)
    const points = []
    for(let i = 0; i < new_arr.length; i += 2) {
        points.push({
            coords: [new_arr[i], new_arr[i + 1]],
            value: (Math.random() * 3000).toFixed(2),
        })
    }
    return points
}
